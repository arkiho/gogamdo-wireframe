/**
 * 인사이트 마크다운 정리 유틸
 * AI가 숫자 범위를 "3~~4주"처럼 물결표 2개로 쓰면 마크다운이 취소선으로 렌더합니다.
 * 저장된 콘텐츠를 건드리지 않고, 렌더 직전에 이런 의도치 않은 취소선을 정리합니다.
 */

/**
 * 물결표 2개 이상(~~, ~~~ ...)을 하나(~)로 축약해 의도치 않은 취소선을 방지합니다.
 * B2B 인사이트 본문에서 실제 취소선을 쓰는 경우는 없으므로 안전합니다.
 */
export function sanitizeInsightMarkdown(md: string | null | undefined): string {
  if (!md) return "";
  return md
    // 전각 물결(～/〜)도 일반 물결로 정규화
    .replace(/[～〜]/g, "~")
    // 물결표 2개 이상 → 1개 (취소선 방지)
    .replace(/~{2,}/g, "~");
}
